TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<ustring name,ustring value,rstring arithmeticExpression>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("name");
        TypeOffset t(MY_OFFSETOF(name_, tm), 
                     Meta::Type::typeOf<SPL::ustring >(), 
                     &typeid(SPL::ustring));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("value");
        TypeOffset t(MY_OFFSETOF(value_, tm), 
                     Meta::Type::typeOf<SPL::ustring >(), 
                     &typeid(SPL::ustring));
        addMapping(*tm, t, s, 1);
    }
    
    {
        std::string s("arithmeticExpression");
        TypeOffset t(MY_OFFSETOF(arithmeticExpression_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 2);
    }
    
    return tm;
}

